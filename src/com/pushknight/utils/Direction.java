package com.pushknight.utils;

/**
 * Enum representing movement directions.
 * Used for player movement and entity positioning.
 */
public enum Direction {
    /** Move up (negative Y) */
    UP,
    
    /** Move down (positive Y) */
    DOWN,
    
    /** Move left (negative X) */
    LEFT,
    
    /** Move right (positive X) */
    RIGHT,
    
    /** No movement */
    NONE
}

