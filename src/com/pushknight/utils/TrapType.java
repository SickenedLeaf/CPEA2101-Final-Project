package com.pushknight.utils;

/**
 * Enum representing different types of traps in the game.
 * Used to identify trap types for creation and behavior.
 */
public enum TrapType {
    /**
     * Spike trap that deals instant damage to enemies.
     */
    SPIKE,
    
    /**
     * Slow trap that reduces enemy movement speed.
     * (For future implementation)
     */
    SLOW,
    
    /**
     * Explosive trap that deals area of effect damage.
     * (For future implementation)
     */
    EXPLOSIVE
}

